// lib/models/detection.dart

class Detection {
  final String className;
  final double confidence;
  final List<double> bbox; // [x, y, width, height]
  final double distance; // estimated distance in meters

  Detection({
    required this.className,
    required this.confidence,
    required this.bbox,
    required this.distance,
  });

  @override
  String toString() => '$className (${(confidence * 100).toStringAsFixed(1)}%) - ${distance.toStringAsFixed(1)}m away';
}

